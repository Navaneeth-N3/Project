package com.package4;

public class Day4flow8 {
	public static void main(String[] args) {
		System.out.println("main begin");
		for(int i=10;i<12;i++) { 
			System.out.println("value of i="+i);// Sysout gets printed after condition check is true and then goes to increment
		}
		System.out.println("main end " );
	}

}
/*for loop has four components 
 1.initialization int i =0 -  it will be executed only once
 2.condition check i<10 - it will be done always
 if condition check is true then for loop body will execute
 else, if it is false for loop will terminate+i
 after for loop body increment or decrement section will execute
 after decrement or increment again condition check will happen
 true it will execute false it will terminate
 
 3.increment or decrement i++ or i--, i=i+2 etc
 4.for loop body -  between the curly brackets
 */
 
 

