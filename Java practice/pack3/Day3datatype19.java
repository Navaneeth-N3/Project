package com.pack3;

public class Day3datatype19 {
	public static void main(String[] args) {
		int i=32;
		System.out.println(++i);// increments by 1 before printing and then prints  33
		System.out.println("After increment value of i="+i);//prints 33
		
		
		System.out.println(--i);//decrements by 1 before printing and then prints 32
		System.out.println("After Decrement"+i);//prints 32
	}

}
