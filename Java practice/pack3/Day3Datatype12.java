package com.pack3;

public class Day3Datatype12 {
	public static void main(String[] args) {
		int i =43,j=67;
		System.out.println(i);
		System.out.println(j);
		i=i+j;//i=110
		j=i-j;//110-67=43
		i=i-j;//i=67
		
		
		System.out.println("After swapping i="+i);
		System.out.println("After swapping j="+j);
		
		
		
	}

}
